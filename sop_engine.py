import json
import os
from openai import OpenAI

client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))


def generate_sop_data(text, department, language="Arabic", mode="extract"):
    mode_instruction = ""
    if mode == "rewrite":
        mode_instruction = """
- المطلوب هو إعادة صياغة المحتوى بشكل مهني ورسمي ليتوافق مع النموذج الرسمي المختصر المعتمد.
- يجب تكثيف المحتوى داخل الحقول المتاحة فقط دون الإخلال بالمعنى.
- لا تختصر اختصاراً شديداً.
"""
    else:
        mode_instruction = """
- المطلوب هو استخراج المحتوى من النص المرفوع وإعادة تنظيمه ليتوافق مع النموذج الرسمي المختصر المعتمد.
- يجب تكثيف المحتوى داخل الحقول المتاحة فقط دون الإخلال بالمعنى.
- لا تختصر اختصاراً شديداً.
"""

    prompt = f"""
أنت خبير محترف في إعداد الإجراءات التشغيلية القياسية في البنوك.

النموذج النهائي ثابت ورسمي ولا يمكن تغييره.
يجب إعادة تنظيم النص ليتناسب فقط مع الحقول التالية:
- عنوان الإجراء
- المقدمة
- الأنظمة المشاركة
- هدف الإجراء
- منطق المطابقة
- 3 خطوات فقط داخل جدول الإجراء

لكن مهم جداً:
- المطلوب ليس تلخيصاً قصيراً جداً.
- يجب الاحتفاظ بأكبر قدر ممكن من محتوى النص الأصلي.
- يجب ضغط التفاصيل داخل الحقول الموجودة فقط.
- إذا كان النص يحتوي على نطاق تطبيق أو تعريف أو مسؤوليات أو ضوابط أو تقارير أو مستندات، فيجب دمجها داخل الحقول المتاحة، وليس حذفها.

طريقة الدمج:
- "المقدمة" = الهدف العام + نطاق التطبيق + التعريف + ملخص المعالجة المحاسبية إن وجد.
- "الأنظمة المشاركة" = الأنظمة أو السجلات أو الأدوات الرئيسية فقط.
- "هدف الإجراء" = جميع الأهداف الجوهرية في نقاط واضحة.
- "منطق المطابقة" = الشروط + الضوابط + معايير التحقق + نقاط الرقابة.
- "steps" = 3 خطوات فقط، لكن كل خطوة يمكن أن تحتوي على عدة أعمال فرعية مدمجة بصياغة مهنية.
- لا تجعل الخطوات قصيرة جداً.
- اجعل كل خطوة غنية بالتفاصيل قدر الإمكان ما دام ذلك مناسباً للجدول.
- "tools" يجب أن تجمع أكبر عدد مناسب من المستندات/النماذج المتعلقة بكل خطوة.

{mode_instruction}

أعد النتيجة بصيغة JSON فقط، بهذا الشكل تماماً:

{{
  "title": "",
  "code": "",
  "department": "{department}",
  "version": "01",
  "issue_date": "",
  "circular_date": "",
  "approval_entity": "",
  "approval_date": "",
  "signature": "",
  "main_references": "",
  "used_forms": "",
  "procedure_owner": "",
  "quality_management": "",
  "authorized_manager": "",
  "introduction": "",
  "systems": [""],
  "objective": [""],
  "matching_logic": [""],
  "steps": [
    {{
      "no": "1",
      "responsibility": "",
      "procedure": "",
      "tools": ""
    }},
    {{
      "no": "2",
      "responsibility": "",
      "procedure": "",
      "tools": ""
    }},
    {{
      "no": "3",
      "responsibility": "",
      "procedure": "",
      "tools": ""
    }}
  ]
}}

التعليمات:
- جميع المخرجات بالعربية الرسمية فقط.
- "title" هو عنوان الإجراء الفعلي.
- "code" يترك فارغاً إذا لم يذكر في النص.
- "introduction" يجب أن تكون فقرة غنية ومتكاملة، وليست سطراً قصيراً.
- "systems" نقاط واضحة بدون ترقيم.
- "objective" نقاط واضحة بدون ترقيم.
- "matching_logic" نقاط واضحة بدون ترقيم.
- "steps" يجب أن تكون 3 فقط لا غير.

لكن مهم جداً في "steps":
- كل خطوة يجب أن تحتوي على عدة إجراءات فرعية مدمجة.
- لا تجعل الخطوة جملة واحدة قصيرة.
- يجب أن تكون كل خطوة فقرة تفصيلية تحتوي على جميع الأنشطة المرتبطة بها.
- لا تستخدم نقاط Bullet داخل حقل "procedure".
- اكتب الإجراء كسرد مهني متصل باستخدام الفواصل أو الفاصلة المنقوطة عند الحاجة.
- يجب أن تكون الخطوات طويلة وغنية بالتفاصيل وتعكس كامل محتوى النص الأصلي قدر الإمكان.

تقسيم الخطوات:
- الخطوة 1 تشمل: الاستلام + التحقق + المراجعة الأولية + جمع المستندات + التأكد من الشروط + التحقق من فترة الاستفادة والبيانات الأساسية.
- الخطوة 2 تشمل: التقييم + المعالجة + التسجيل المحاسبي + إعداد الجداول + تحديد الفترات + احتساب القيم الدورية + تنفيذ المعالجة الأساسية.
- الخطوة 3 تشمل: المتابعة + المراجعة + المطابقة + الإطفاء + التسويات + التقارير + الإقفال + التأكد من تصفير الرصيد عند انتهاء الفترة.

- إذا كان النص يحتوي على تفاصيل كثيرة، وزعها بذكاء على الخطوات الثلاث دون حذف.
- "tools" يجب أن تكون غنية قدر الإمكان ومأخوذة من النص، ويمكن أن تحتوي على أكثر من أداة أو مستند في السطر نفسه.
- "responsibility" يجب أن تعكس الجهة الأساسية المسؤولة عن كل خطوة.
- لا تكتب أي نص خارج JSON.

النص:
{text}
"""

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.15,
    )

    content = response.choices[0].message.content.strip()

    if content.startswith("```json"):
        content = content.replace("```json", "").replace("```", "").strip()
    elif content.startswith("```"):
        content = content.replace("```", "").strip()

    return json.loads(content)
